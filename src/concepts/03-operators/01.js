/**
% : Modulus (remainder)
 */

const num1 = 5;
const num2 = 10;

const addition = num1 + num2;
console.log(addition);

const subtraction = num1 - num2;
console.log(subtraction);

const multiplication = num1 * num2;
console.log(multiplication);

const division = num1 / num2;
console.log(division);

const modulus = num1 % num2;
console.log(modulus);

// Unary plus
const positiveNumber = +"30";
console.log(positiveNumber);

// Unary negation
const negativeNumber = -"30";
console.log(negativeNumber);

// Exponent
const bits = 2 ** 3;
console.log(bits);

// Increment
let x = 4;
let preIncrement = ++x;
console.log(preIncrement);
console.log(x);

let y = 4;
let postIncrement = y++;
console.log(postIncrement);
console.log(y);

// Decrement
let a = 4;
let preDecrement = --a;
console.log(preDecrement);
console.log(a);

let b = 4;
let postDecrement = b--;
console.log(postDecrement);
console.log(b);

// Remainder (Modulus)
const remainder = 20 % 8;
console.log(remainder);

// Comparison operators
// Equal
console.log(3 == "3");

// Not equal
console.log(3 != "3");

// Strict equal
console.log(3 === 3);

// Strict not equal
console.log(3 !== 3);

// Greater than
console.log(5 > 5);

// Greater than or equal
console.log(5 >= 5);

// Lesser than
console.log(5 < 5);

// Lesser than or equal
console.log(5 <= 5);
