// Assignment operator
const fullName = "Bob Smith";
console.log(fullName);

// Arithmetic operators
// add
const addition = 3 + 2;
console.log(addition);

// subtract
const subtraction = 3 - 2;
console.log(subtraction);

// multiply
const multiplication = 3 * 2;
console.log(multiplication);

// divide
const division = 3 / 2;
console.log(division);

// Unary plus
const positiveNumber = +"30";
console.log(positiveNumber);

// Unary negation
const negativeNumber = -"30";
console.log(negativeNumber);

// Exponent
const bits = 2 ** 3;
console.log(bits);

// Increment
let x = 4;
let preIncrement = ++x;
console.log(preIncrement);

let y = 4;
let postIncrement = y++;
console.log(postIncrement);

// Decrement
let a = 4;
let preDecrement = --a;
console.log(preDecrement);

let b = 4;
let postDecrement = b--;
console.log(postDecrement);

// Remainder (Modulus)
const remainder = 20 % 8;
console.log(remainder);

// Comparison operators
// Equal
console.log(3 == "3");

// Not equal
console.log(3 != "3");

// Strict equal
console.log(3 === 3);

// Strict not equal
console.log(3 !== 3);

// Greater than
console.log(5 > 5);

// Greater than or equal
console.log(5 >= 5);

// Lesser than
console.log(5 < 5);

// Lesser than or equal
console.log(5 <= 5);
